#include "CRequestUrl.h"

CRequestUrl::CRequestUrl()
{
	m_url.clear();
	m_filename.clear();
}

CRequestUrl::~CRequestUrl()
{
	m_url.clear();
	m_filename.clear();
}

void CRequestUrl::setCRequestUrl(const string &url)
{
	m_url = url;
}

string CRequestUrl::getCRequestUrl()
{
	return m_url;
}

void CRequestUrl::setFilename(const string &filename)
{
	m_filename = filename;
}

string CRequestUrl::getFilename()
{
	return m_filename;
}

